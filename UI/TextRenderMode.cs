namespace BazthalLib.UI
{
    /// <summary>
    /// Specifies the rendering mode for text alignment.
    /// </summary>
    /// <remarks>This enumeration defines the different ways text can be aligned when rendered.</remarks>
    public enum TextRenderMode
    {
        None,           // Do not draw text
        Centered,       // Centered horizontally and vertically
        TopLeft,        // Align top-left (basic default)
                        //Custom          // Allow override of alignment logic
    }
}


